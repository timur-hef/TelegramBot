def translate(text):
    try:
        response = requests.post(
            url='https://libretranslate.de/translate',
            params={
                'format': 'text',
                'target': 'en',
                'source': 'ru',
                'q': text
            }
        )

        translated = response.json()
        result = translated['translatedText']
    except Exception as e:
        logger.error(f'–ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–≤–µ—Å—Ç–∏ –Ω–∞–∏–º–µ–Ω–æ–≤–∞–Ω–∏–µ: {text}')
        raise Exception

    return result


